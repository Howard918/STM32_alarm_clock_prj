void Time_Increment(TimeTypeDef *time)
{
    time->seconds++;

    if(time->seconds >= 60)
    {
        time->seconds = 0;
        time->minutes++;

        if(time->minutes >= 60)
        {
            time->minutes = 0;
            time->hours++;

            if(time->hours >= 24)
            {
                time->hours = 0;
            }
        }
    }
}

